// ---------------------------------------------------------------------------
// Legacy WAV lookup
// ---------------------------------------------------------------------------

async function tryPreGenerated(slideId: string): Promise<HTMLAudioElement | null> {
  const url = `/demo-audio/${slideId}.wav`;
  try {
    const resp = await fetch(url, { method: "HEAD" });
    if (resp.ok) return loadAudio(url);
  } catch {
    // Pre-generated file doesn't exist
  }
  return null;
}

// ---------------------------------------------------------------------------
// Real-time TTS
// ---------------------------------------------------------------------------

async function generateRealTime(text: string): Promise<HTMLAudioElement | null> {
  try {
    const resp = await fetch("/api/demo/tts", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ text }),
    });

    if (!resp.ok) return null;

    const blob = await resp.blob();
    const url = URL.createObjectURL(blob);
    return loadAudio(url);
  } catch {
    return null;
  }
}

// ---------------------------------------------------------------------------
// Shared audio loader
// ---------------------------------------------------------------------------

function loadAudio(url: string): Promise<HTMLAudioElement | null> {
  return new Promise((resolve) => {
    const audio = new Audio(url);
    audio.crossOrigin = "anonymous";
    audio.addEventListener("canplaythrough", () => resolve(audio), { once: true });
    audio.addEventListener("error", () => resolve(null), { once: true });
  });
}

/**
 * Wire an HTMLAudioElement through Web Audio API to get an AnalyserNode.
 * createMediaElementSource can only be called once per element, so this
 * must happen before caching.
 */
function wireAnalyser(audio: HTMLAudioElement): AnalyserNode {
  const ctx = getAudioContext();
  // Resume context (required after user gesture)
  if (ctx.state === "suspended") {
    ctx.resume().catch(() => {});
  }
  const source = ctx.createMediaElementSource(audio);
  const analyser = ctx.createAnalyser();
  analyser.fftSize = 256;
  source.connect(analyser);
  analyser.connect(ctx.destination);
  return analyser;
}

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

/**
 * Get audio + analyser for a demo slide. Returns null if TTS is unavailable.
 * Results are cached for the session.
 */
export async function getDemoAudio(
  slideId: string,
  narrationText: string,
): Promise<DemoAudioResult | null> {
  if (audioCache.has(slideId)) {
    const cached = audioCache.get(slideId)!;
    cached.audio.currentTime = 0;
    return cached;
  }

  // Try manifest MP3 → legacy WAV → real-time TTS
  const audio =
    (await tryManifest(slideId)) ??
    (await tryPreGenerated(slideId)) ??
    (await generateRealTime(narrationText));

  if (audio) {
    const analyser = wireAnalyser(audio);
    const result = { audio, analyser };
    audioCache.set(slideId, result);
    return result;
  }
  return null;
}
