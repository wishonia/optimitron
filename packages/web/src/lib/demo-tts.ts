/**
 * Client-side TTS helper for the demo player.
 *
 * Priority:
 *   1. Narration manifest (pre-generated MP3s in /audio/narration/)
 *   2. Legacy pre-generated WAV (/demo-audio/)
 *   3. Real-time Gemini TTS API
 *   4. Subtitle-only (graceful degradation)
 *
 * Returns both an HTMLAudioElement and an AnalyserNode for lip-sync.
 */

export interface DemoAudioResult {
  audio: HTMLAudioElement;
  analyser: AnalyserNode;
}

const audioCache = new Map<string, DemoAudioResult>();

// Shared AudioContext — reused across slides (browsers limit creation)
let sharedCtx: AudioContext | null = null;

function getAudioContext(): AudioContext {
  if (!sharedCtx) {
    sharedCtx = new AudioContext();
  }
  if (sharedCtx.state === "suspended") {
    sharedCtx.resume().catch(() => {});
  }
  return sharedCtx;
}

// ---------------------------------------------------------------------------
// Manifest-based lookup (MP3s generated by generate-narration script)
// ---------------------------------------------------------------------------

type ManifestEntry = { hash: string; file: string; generatedAt: string };
type Manifest = Record<string, ManifestEntry>;

let manifestPromise: Promise<Manifest | null> | null = null;

function getManifest(): Promise<Manifest | null> {
  if (!manifestPromise) {
    manifestPromise = fetch("/audio/narration/manifest.json")
      .then((r) => (r.ok ? r.json() : null))
      .catch(() => null);
  }
  return manifestPromise;
}

/** Try to load from the narration manifest (MP3 files) */
async function tryManifest(slideId: string): Promise<HTMLAudioElement | null> {
  const manifest = await getManifest();
  if (!manifest) return null;

  const keysToTry = [slideId];

  if (segmentToSlideId[slideId]) {
    const gameSlideId = segmentToSlideId[slideId];
    keysToTry.push(`protocol-labs--${gameSlideId}`, gameSlideId);
  }

  for (const key of keysToTry) {
    const entry = manifest[key];
    if (entry) {
      return loadAudio(`/audio/narration/${entry.file}`);
    }
  }

  return null;
}

