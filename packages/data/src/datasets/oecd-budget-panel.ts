// ─── Helper to construct rows ─────────────────────────────────────────

/** Derive per-capita PPP: percentGdp × gdpPerCapitaPpp / 100, rounded to nearest int */
function toPerCapitaPpp(percentGdp: number | null, gdpPerCapita: number | null): number | null {
  return percentGdp != null && gdpPerCapita != null ? Math.round(percentGdp * gdpPerCapita / 100) : null;
}

function row(
  jurisdictionIso3: string,
  year: number,
  health: number | null,
  education: number | null,
  military: number | null,
  social: number | null,
  rd: number | null,
  lifeExp: number | null,
  gdpPc: number | null,
  infantMort: number | null,
  gini: number | null,
): OECDBudgetPanelDataPoint {
  return {
    jurisdictionIso3,
    year,
    healthSpendingPercentGdp: health,
    educationSpendingPercentGdp: education,
    militarySpendingPercentGdp: military,
    socialSpendingPercentGdp: social,
    rdSpendingPercentGdp: rd,
    healthSpendingPerCapitaPpp: toPerCapitaPpp(health, gdpPc),
    educationSpendingPerCapitaPpp: toPerCapitaPpp(education, gdpPc),
    militarySpendingPerCapitaPpp: toPerCapitaPpp(military, gdpPc),
    socialSpendingPerCapitaPpp: toPerCapitaPpp(social, gdpPc),
    rdSpendingPerCapitaPpp: toPerCapitaPpp(rd, gdpPc),
    lifeExpectancyYears: lifeExp,
    gdpPerCapitaPpp: gdpPc,
    infantMortalityPer1000: infantMort,
    giniIndex: gini,
  };
}

